const TextUtils = {
  isEmail: (email) => {
    return rEmail.test(email);
  },
  generateQS: (data) => {
    const jaId = data.jobActivityId ? data.jobActivityId : '';
    const type = data.templateType ? data.templateType : '';
    const params = [];
    jaId && (params.push(`jaId=${jaId}`));
    type && (params.push(`type=${type}`));
    return params.join('&');
  },
  getQueryParams: (path = '') => {
    const str = path.substr(1, path.length);
    const ret = {};
    if (!path) {
      return ret;
    }
    const queryArray = str.split('&');
    for (let i = 0; i < queryArray.length; i++) {
      const param = queryArray[i].split('=');
      ret[param[0]] = param[1];
    }
    return ret;
  },

};
export default TextUtils;
